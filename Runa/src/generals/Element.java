package generals;

/**
 * This enumeration contains all Elements existing, including 'None'
 */
public enum Element {
    /**
     * Element water
     */
    WATER,
    /**
     * Element ice
     */
    ICE,
    /**
     * Element fire
     */
    FIRE,
    /**
     * Element lightning
     */
    LIGHTNING,
    /**
     * no element
     */
    NONE
}
